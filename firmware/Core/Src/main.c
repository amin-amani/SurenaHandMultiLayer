void hw_set_direction(bool dir)
{
	GPIOB->ODR &=~(1<<12);
	if(dir)GPIOB->ODR |=(1<<12);
}
//value is between 0 to 5
void hw_select_driver_motor(uint8_t value)
{
	value&=0x07;
	GPIOB->ODR &=~(0x07<<13);
	GPIOB->ODR |=(value<<13);
}
