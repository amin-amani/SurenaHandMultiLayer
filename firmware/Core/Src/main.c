void hw_set_motor_speed(uint8_t motor,int32_t speed )
{


	if(speed<0) {
		hw_set_direction(0);
		speed*=-1;
	}
	else{
		hw_set_direction(1);

	}
	speed=1900-speed;
	if(speed>1900)speed=1900;
	if(speed<2)speed =2;

	hw_select_driver_motor(motor);

	 TIM4->CCR3=speed;



}

